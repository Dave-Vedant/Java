package vedant.learningPhase;

public class Ceiling {
    private int height;
    private int printedColor;

    public Ceiling(int height, int printedColor) {
        this.height = height;
        this.printedColor = printedColor;
    }

    public int getHeight() {
        return height;
    }

    public int getPrintedColor() {
        return printedColor;
    }
}
